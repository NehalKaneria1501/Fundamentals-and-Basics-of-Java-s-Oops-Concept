public class DoubleAverage
{
    public static void main(String[]args)
    {
        int [][]Average={{9,2,6,5,7},{5,4,1,6,1}};
        {
            for(int i=0;i<=5;i++)
            {
                for(int j=0;j<=5;i++)
                {
                    System.out.println(Average[i][j]);
                }
            }
        }
    }
}