public class MergeOperation
{
    public static void main(String[]args)
    {
        int [][]Merge={{9,2,6,5,7},{5,4,1,6,1}};
        {
            for(int i=0;i<=4;i++)
            {
                for(int j=0;j<=4;j++)
                {
                    System.out.println(Merge[i][j]);
                }
            }
        }
    }
}