public class AdditionArray
{
    public static void main(String[]args)
    {
        int [][] Addition={{1,3,11,13,23},{1,3,10,11,13}};
        for(int i=0;i<=4;i++)
        {
            for(int j=0;j<=4;j++)
            {
                System.out.println(Addition[i][j]);
            }
        }
    }
}