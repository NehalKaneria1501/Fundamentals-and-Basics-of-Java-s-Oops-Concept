public class ExplicitDataTypeConversion
{
    public static void main(String[] args)
    {
        double a=50.50;
        System.out.println("Double representation"+a);
        float f=(float)a;
        System.out.println("Float representsation"+f);
        long b=(long)a;
        System.out.println("Long representation"+b);
        int c=(int)b;
        System.out.println("FLoat representarion"+c);
    }
}