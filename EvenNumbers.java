public class EvenNumbers
{
    public static void main(String[]args)
    {
        int [][]Even={{2000,2002,2004,2006,2008,2010},{2012,2014,2016,2018,2020}};
        {
            for(int i=0;i<=5;i++)
            {
                for(int j=0;j<=5;j++)
                {
                    System.out.println(Even[i][j]);
                }
            }
        }
    }
}